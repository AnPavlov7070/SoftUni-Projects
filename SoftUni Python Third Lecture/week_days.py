days_of_the_week = int(input())

if days_of_the_week == 1:
    print("Monday")
elif days_of_the_week == 2:
    print("Tuesday")
elif days_of_the_week == 3:
    print("Wednesday")
elif days_of_the_week == 4:
    print("Thursday")
elif days_of_the_week == 5:
    print("Friday")
elif days_of_the_week == 6:
    print("Saturday")
elif days_of_the_week == 7:
    print("Sunday")

else:
    print("Error")
    